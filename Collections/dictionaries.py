# Dictionary example
student_record= {"name" : "spandana", "age" : 20, "marks" : 90}
student_record["marks"] = 97                                           
student_record["grade"] = "B"
print("student record is :", student_record)


# Using Updating values and adding key and values 
employee = {"id" : 74, "name" : "Spandana", "department" : "IT", "salary" : 90000}
print("Employee Details : ", employee)
print("Employee Name is :", employee["name"])
employee["city"] = "Nellore"
print("After Adding City :", employee)
employee["salary"] = 1000000
print("After Updating Salary :", employee)

