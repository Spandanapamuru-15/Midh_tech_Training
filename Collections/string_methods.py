# Common String methods
course = "python@programming"
print("lower : ", course.lower()) #lower method
print("upper : ", course.upper()) #Upper Method
print("strip : ", course.strip()) #Strip Method
print("split : ", course.split("@")) #Split Method
print("replace : ", course.replace("p", "S")) #Replace Method
print("find : ", course.find("n")) #Find Method
print("count : ", course.count("t")) #Count Method
print(course.startswith("p")) #Startswith Method
print(course.endswith("n")) #endswith Method




